function y = play_tune()
    
    % --- load tune ----------------------------
    % ------------------------------------------
    load('odetojoy');
    % ------------------------------------------

    % --- calculate freq -----------------------
    % ------------------------------------------    
    Fs = 8000;              % samples per second
    dt = 1/Fs;              % seconds per sample
    N = length(y);
    dF = Fs/N;              % hertz
    f = -Fs/2:dF:Fs/2-dF;   % hertz
    % ------------------------------------------

    % --- Run Fourier Transform ----------------
    % ------------------------------------------
    X = fftshift(fft(y));
    % ------------------------------------------

    % --- Create plot --------------------------
    % ------------------------------------------
    plot(f,abs(X)/N);
    xlabel('Frequency');
    ylabel('Magnitude');
    title('FFT of Ode To Joy');
    % ------------------------------------------

end

